from math import sin, cos, tan, radians
angulo = float(input("Digite o 창ngulo em graus: "))
print(f'O 창ngulo de {angulo} tem o SENO de {sin(radians(angulo)):.2f}')
print(f'O 창ngulo de {angulo} tem o COSSENO de {cos(radians(angulo)):.2f}')
print(f'O 창ngulo de {angulo} tem a TANGENTE de {tan(radians(angulo)):.2f}')
